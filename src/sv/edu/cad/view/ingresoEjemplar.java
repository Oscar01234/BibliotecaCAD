    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(lblID.getText().equals("[Seleccione ID]"))
        {
            JOptionPane.showMessageDialog(this, "Seleccione un ID para el ingreso");
        }
        else if(txtCantidad.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Indique la cantidad de ejemplares a ingresar");
        }
        else if(Integer.parseInt(txtCantidad.getText())<=0){
            JOptionPane.showMessageDialog(this, "Debe ingresar al menos un ejemplar");
        }
        else
        {
            int idCatalogo = Integer.parseInt(lblID.getText());
            int idTiempo = cmbPrestamo.getSelectedIndex()+1;
            int Cantidad = Integer.parseInt(txtCantidad.getText());
            ejemplar.ingresoEjemplares(idCatalogo, idTiempo, Cantidad);
            txtCantidad.setText("");
            cmbPrestamo.setSelectedIndex(0);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void txtCantidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadKeyTyped
        //Para solo digitar enteros en cantidad
        if(!Character.isDigit(evt.getKeyChar())){
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_txtCantidadKeyTyped


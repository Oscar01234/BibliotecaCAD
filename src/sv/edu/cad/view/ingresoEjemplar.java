    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(txtIDCatalogo.getText().equals("")==false){
            Object [] valores = ejemplar.devuelveItem(Integer.parseInt(txtIDCatalogo.getText()));
            if(valores != null)
            {
                lblID.setText(valores[0].toString());
                lblTitulo.setText(valores[1].toString());
                lblMaterial.setText(valores[2].toString());
                txtCantidad.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "No se encontro el registro "+txtIDCatalogo.getText());
            }
        }
        else
            JOptionPane.showMessageDialog(this, "Ingrese un ID");
    }//GEN-LAST:event_jButton1ActionPerformed

